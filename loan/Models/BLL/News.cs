        public List<Pan.Model.News> DataTableToList(DataTable dt)
        {
            List<Pan.Model.News> modelList = new List<Pan.Model.News>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                Pan.Model.News model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

